package com.Final;

public class House {
    String name;
    int purchasePrice;
    int sellPrice;
    boolean starterHouse;

    House(String name, int purchasePrice, int sellPrice, boolean starterHouse){
        this.name = name;
        this.purchasePrice = purchasePrice;
        this.sellPrice = sellPrice;
        this.starterHouse = starterHouse;
    }
}
